from collections import deque
class MyQueue(object):
    
    def __init__(self):
        self.queue=[]
   
       
    def push(self, x):
        """
        :type x: int
        :rtype: None
        """
        self.queue.append(x)

    def pop(self):
        """
        :rtype: int
        """
        a = self.queue[0]
        del self.queue[0]
        return a
        
    def peek(self):
        """
        :rtype: int
        """
        return self.queue[0]

    def empty(self):
        """
        :rtype: bool
        """
        if self.queue==[]:
            return True
        



